package helseapp.core;

public class Dag{

    private double vekt;
    private double hoyde;

    public Dag(double vekt, double hoyde) {
        this.vekt = vekt;
        this.hoyde = hoyde;
    }

    public double getVekt() {
        return vekt;
    }

    public double getHoyde() {
        return hoyde;
    }
}